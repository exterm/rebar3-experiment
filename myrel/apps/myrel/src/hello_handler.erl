%%%-------------------------------------------------------------------
%% @doc example request handler for cowboy
%% @end
%%%-------------------------------------------------------------------

-module(hello_handler).

%% Application callbacks
-export([init/3
        ,handle/2
        ,terminate/3]).

%%====================================================================
%% API
%%====================================================================

init(_Type, Req, []) ->
	{ok, Req, undefined}.

%%--------------------------------------------------------------------

handle(Req, State) ->
	{ok, Req2} = cowboy_req:reply(200, [
		{<<"content-type">>, <<"text/plain">>}
	], <<"Hello world!\n">>, Req),
	{ok, Req2, State}.

%%--------------------------------------------------------------------

terminate(_Reason, _Req, _State) ->
	ok.

